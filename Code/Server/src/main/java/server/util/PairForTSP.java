package server.util;

public class PairForTSP {
    private double first;
    private int second;
    public PairForTSP() {
        this.first = 0;
        this.second = 0;
    }

    public double getFirst() {
        return first;
    }

    public void setFirst(double first) {
        this.first = first;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }
}
